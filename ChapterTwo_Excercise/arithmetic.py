first_input = int(input("Enter your first number : "))
second_input = int(input("Enter second  number : "))
square_a = first_input * first_input
square_b = second_input * second_input
sum_of_squares = square_a + square_b
difference_of_squares = square_a - square_b
print(f"The square of first number entered is : {square_a}")
print(f"The square of second number enterd is : {square_b}")
print(f"The sum of squares of both numbers is : {sum_of_squares}")
print(f"The difference of both squares entered is : {difference_of_squares}")
